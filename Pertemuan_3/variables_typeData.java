public class variables_typeData {
    public static void main(String[] args) {
        
        // Pembentukan Variable
        String name = "Imam Pamuji"; // Type data string / Text
        int age = 18;   // Type data integer / Angka bulat
        double height = 180.5; // Type data double / bilangan desimal, dengan ketepatan hingga 15 digit

        // Panggil variabel di dalam perintah print
        System.out.println("Nama saya " + name);
        System.out.println("Saya berumur " + age + " tahun");
        System.out.println("Tinggi badan saya " + height + " CM");
       
        
    }
}
